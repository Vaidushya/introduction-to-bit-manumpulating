# Program to showcase working of different types of bitwise operators

num1 = 10
num2 = 4

# using NOT operators
print(f"~num1 = {~num1}")
print(f"\n ~num2 = {~num2}")

# using AND operators
print(f"\n num1 & num2 = {num1 & num2}")

# using OR operators
print(f"\n num1 | num2 = {num1 | num2}")

# using XOR operators
print(f"\n num1 ^ num2 = {num1 ^ num2}")

# using left shift operators on num1
print(f"\n num1 << 2 = {num1 << 1}")

# using left shift operators on num2
print(f"\n num2 << 1 = {num2 << 1}")

# using right shift operators
print(f"\n num1 >> 1 = {num1 >> 1}")

# using right shift on num2
print(f"\n num2 >> 1 = {num2 >> 1}")
